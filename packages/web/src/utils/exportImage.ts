import type { Message } from '../stores/sessionStore'

/** Canvas 导出配置 */
const CONFIG = {
  width: 720,
  padding: 16,
  headerHeight: 56,
  msgPadding: 12,
  msgGap: 8,
  maxLineChars: 55,
  lineHeight: 20,
  fontSize: 14,
  bgColor: '#0a0a0a',
  headerBg: '#111111',
  userBubbleBg: '#2563eb',
  assistantBubbleBg: '#1e1e1e',
  textColor: '#e5e5e5',
  mutedColor: '#999999',
  borderRadius: 12,
}

/**
 * 将消息内容按字符宽度换行
 * 简化处理：中文字符约等于 2 个英文字符宽度
 */
function wrapText(text: string, maxChars: number): string[] {
  // 先将代码块替换为 [代码块]
  const cleaned = text
    .replace(/```[\s\S]*?```/g, '[代码块]')
    .replace(/`[^`]+`/g, (m) => m.slice(1, -1))
    .replace(/!\[.*?\]\(.*?\)/g, '[图片]')
    .replace(/\[([^\]]+)\]\(.*?\)/g, '$1')
    .replace(/[#*_~>]/g, '')

  const lines: string[] = []
  for (const paragraph of cleaned.split('\n')) {
    if (paragraph.trim() === '') {
      lines.push('')
      continue
    }
    let current = ''
    let currentWidth = 0
    for (const char of paragraph) {
      const charWidth = char.charCodeAt(0) > 127 ? 2 : 1
      if (currentWidth + charWidth > maxChars) {
        lines.push(current)
        current = char
        currentWidth = charWidth
      } else {
        current += char
        currentWidth += charWidth
      }
    }
    if (current) lines.push(current)
  }
  return lines
}

/**
 * 计算消息气泡高度
 */
function calcMessageHeight(text: string): number {
  const lines = wrapText(text, CONFIG.maxLineChars)
  return CONFIG.msgPadding * 2 + lines.length * CONFIG.lineHeight + 16 // 16 = role label height
}

/**
 * 在 Canvas 上绘制圆角矩形
 */
function roundRect(ctx: CanvasRenderingContext2D, x: number, y: number, w: number, h: number, r: number) {
  ctx.beginPath()
  ctx.moveTo(x + r, y)
  ctx.lineTo(x + w - r, y)
  ctx.quadraticCurveTo(x + w, y, x + w, y + r)
  ctx.lineTo(x + w, y + h - r)
  ctx.quadraticCurveTo(x + w, y + h, x + w - r, y + h)
  ctx.lineTo(x + r, y + h)
  ctx.quadraticCurveTo(x, y + h, x, y + h - r)
  ctx.lineTo(x, y + r)
  ctx.quadraticCurveTo(x, y, x + r, y)
  ctx.closePath()
  ctx.fill()
}

/**
 * 将会话消息导出为 PNG 长图并触发下载
 */
export function exportChatAsImage(title: string, messages: Message[]) {
  const { width, padding, headerHeight, msgPadding, msgGap, lineHeight, fontSize, bgColor, headerBg, userBubbleBg, assistantBubbleBg, textColor, mutedColor, borderRadius, maxLineChars } = CONFIG

  // 计算总高度
  let totalHeight = headerHeight + padding
  for (const msg of messages) {
    totalHeight += calcMessageHeight(msg.content) + msgGap
  }
  totalHeight += padding // bottom padding

  // 创建 Canvas
  const canvas = document.createElement('canvas')
  const dpr = 2 // 高清
  canvas.width = width * dpr
  canvas.height = totalHeight * dpr
  const ctx = canvas.getContext('2d')!
  ctx.scale(dpr, dpr)

  // 背景
  ctx.fillStyle = bgColor
  ctx.fillRect(0, 0, width, totalHeight)

  // 标题栏
  ctx.fillStyle = headerBg
  ctx.fillRect(0, 0, width, headerHeight)

  ctx.fillStyle = textColor
  ctx.font = `bold ${fontSize + 2}px -apple-system, "Segoe UI", sans-serif`
  ctx.textBaseline = 'middle'
  const displayTitle = title.length > 40 ? title.slice(0, 37) + '...' : title
  ctx.fillText(displayTitle, padding, headerHeight / 2 - 8)

  ctx.fillStyle = mutedColor
  ctx.font = `${fontSize - 2}px -apple-system, "Segoe UI", sans-serif`
  const dateStr = new Date().toLocaleDateString('zh-CN')
  ctx.fillText(`${messages.length} 条消息 · ${dateStr}`, padding, headerHeight / 2 + 12)

  // 绘制消息
  let y = headerHeight + padding
  const bubbleMaxWidth = width - padding * 4

  for (const msg of messages) {
    const lines = wrapText(msg.content, maxLineChars)
    const bubbleHeight = msgPadding * 2 + lines.length * lineHeight + 16
    const isUser = msg.role === 'user'

    // 气泡位置
    const bubbleWidth = Math.min(bubbleMaxWidth, width * 0.75)
    const bubbleX = isUser ? width - padding - bubbleWidth : padding

    // 气泡背景
    ctx.fillStyle = isUser ? userBubbleBg : assistantBubbleBg
    roundRect(ctx, bubbleX, y, bubbleWidth, bubbleHeight, borderRadius)

    // 角色标签
    ctx.fillStyle = mutedColor
    ctx.font = `bold ${fontSize - 3}px -apple-system, "Segoe UI", sans-serif`
    ctx.fillText(
      isUser ? '你' : 'Claude',
      bubbleX + msgPadding,
      y + msgPadding + 4
    )

    // 文本内容
    ctx.fillStyle = textColor
    ctx.font = `${fontSize}px -apple-system, "Segoe UI", sans-serif`
    lines.forEach((line, i) => {
      ctx.fillText(
        line,
        bubbleX + msgPadding,
        y + 16 + msgPadding + i * lineHeight + lineHeight * 0.7
      )
    })

    y += bubbleHeight + msgGap
  }

  // 底部水印
  ctx.fillStyle = mutedColor
  ctx.font = `${fontSize - 3}px -apple-system, "Segoe UI", sans-serif`
  ctx.textAlign = 'center'
  ctx.fillText('Generated by ClaudeCodeChat', width / 2, totalHeight - 8)

  // 触发下载
  canvas.toBlob((blob) => {
    if (!blob) return
    const url = URL.createObjectURL(blob)
    const a = document.createElement('a')
    a.href = url
    a.download = `chat-${title.slice(0, 20)}-${Date.now()}.png`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
  }, 'image/png')
}
